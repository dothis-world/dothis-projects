function randomDate(start, end) {
  return new Date(
    start.getTime() + Math.random() * (end.getTime() - start.getTime()),
  );
}

function randomIntFromRange(min, max) {
  return Math.floor(Math.random() * (max - min + 1) + min);
}

function generateDummyData(data, sourceCount) {
  let newData = [...data];
  newData = newData.map((item) => {
    let newItem = { ...item };
    newItem._source = [];
    for (let i = 0; i < sourceCount; i++) {
      let newSource = {
        channel_id: item._id,
        channel_subscribers: randomIntFromRange(100, 1000),
        channel_total_views: randomIntFromRange(1000, 10000),
        channel_total_videos: randomIntFromRange(100, 500),
        channel_average_views: randomIntFromRange(100, 1000),
        crawled_date: randomDate(
          new Date(2022, 0, 1),
          new Date(),
        ).toISOString(),
      };
      newItem._source.video_list.push(newSource);
    }
    return newItem;
  });
  return newData;
}
