function randomIntFromRange(min, max) {
  return Math.floor(Math.random() * (max - min + 1) + min);
}

function groupVideosByChannel(videos) {
  const result = {};
  videos.forEach((video) => {
    const channelId = video.channel_id;
    if (!result[channelId]) {
      result[channelId] = {
        _id: channelId,
        _source: {
          video_list: [],
          video_aver_views: 0,
          channel_subscribers: randomIntFromRange(5000, 500000),
        },
      };
    }
    const views = randomIntFromRange(1000, 10000); // 1000부터 10000까지의 랜덤한 수
    result[channelId]._source.video_list.push({
      video_id: `video-${randomIntFromRange(1, 100)}`,
      video_tag: video.video_tag,
      crawled_date: video.crawl_update_at,
      video_title: video.video_title,
      video_url: video.video_url,
      views: views,
    });
    result[channelId]._source.video_aver_views =
      (result[channelId]._source.video_aver_views *
        (result[channelId]._source.video_list.length - 1) +
        views) /
      result[channelId]._source.video_list.length;
  });
  return Object.values(result);
}
